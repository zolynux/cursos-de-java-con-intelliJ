public class Ciclo_For
{
    public static void main(String[] args)
    {
        for (var i = 0; i <= 3; i++)
        {
            System.out.println("i = " + i);
        }
        System.out.println();
        for (int i = 0; i < 3; i++)
        {
            System.out.println("i = " + i);
        }
    }
}
