public class Base_Decimal_Hexadecimal_Octal {
    public static void main(String[] args) {
        // Base Decimal
        // byte b1 = 10;
        // int b1 = 10;
        var b1 = 10;
        System.out.println("b1 = " + b1);

        // Base Hexadecimal
        // byte b2 = 0xa;
        // int b2 = 0xa;
        var b2 = 0xa;
        System.out.println("b2 = " + b2);

        // Base Octal
        // byte b3 = 012;
        // int b3 = 012;
        var b3 = 012;
        System.out.println("b3 = " + b3);

        // Resultados exactamentes son 10 mismos informaciones
    }
}
